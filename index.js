//swtich structure

let myValue=100

//standard switch and default
switch (myValue) {
    case 50:
        console.log('Var er 50');
        break;

    case 75:
        console.log('Var er 75');
        break;
    case 100:
        console.log('Var er 100');
        break;

    default:
        console.log('Default');
        break;
}
